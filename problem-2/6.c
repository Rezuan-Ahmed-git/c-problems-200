#include <stdio.h>

int main ()
{
    int m, n;
    printf("Enter an integer: ");
    scanf("%d", &m);

    if(m > 0)
    {
        printf("The value of n = %d", n = 1);
    }
    else if(m == 0)
    {
        printf("The value of n = %d", n = 0);
    }
    else
    {
        printf("The value of n = %d", n = -1);
    }
    return 0;
}
